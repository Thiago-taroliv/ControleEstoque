    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

    
        Produto produto = new Produto();
        
        int id = Integer.parseInt(txtId.getText());
        produto.setNome(txtNome.getText());
        produto.setQuantidade(Integer.parseInt(txtQuantidade.getText()));
        produto.setPreco(Integer.parseInt(txtPreco.getText()));
        produto.setDescricao(txtDescricao.getText());
        
        
        produto.setId(id);
        //inserir tarefa academica no banco de dados:
        ProdutoRepository produtoRepository = new ProdutoRepository();
        boolean retornoBanco = false;
        if(Integer.parseInt(txtId.getText()) == 0){
            //inserir
            retornoBanco = produtoRepository.inserir(
                conexaoMySQL.connection,
                produto);
        }else{
            //atualizar
            retornoBanco = produtoRepository.atualizar(
                conexaoMySQL.connection,
                produto
            );
        }
        if(retornoBanco){
            JOptionPane.showMessageDialog(
                this,
                "Produto cadastrado com sucesso!",
                "Tela de cadastro",
                JOptionPane.INFORMATION_MESSAGE
            );
            limparJanela();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

