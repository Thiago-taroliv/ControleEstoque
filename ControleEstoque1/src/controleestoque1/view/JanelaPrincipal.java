    private void retrocederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_retrocederActionPerformed
        
        limparJanela();
        ProdutoRepository produtoRepository = new ProdutoRepository();
        Produto produto = produtoRepository.selecionar(
                conexaoMySQL.connection, 
                "<", 
                Integer.parseInt(txtId.getText()));
        if(produto != null){
            //jogar os dados da pessoa na tela:
            txtNome.setText(produto.getNome());
            txtQuantidade.setText(String.valueOf(produto.getQuantidade()));
            txtPreco.setText(String.valueOf(produto.getPreco()));
            txtDescricao.setText(produto.getDescricao());
            txtId.setText(String.valueOf(produto.getId()));
        }else{
            limparJanela();
            txtId.setText("0");
        } 
    }//GEN-LAST:event_retrocederActionPerformed

